public class Vertice {
    public Object valor;
    public int indice;

    public Vertice(Object valor, int indice) {
        this.valor = valor;
        this.indice = indice;
    }

    public Object getValor() {
        return valor;
    }

    public void setValor(Object valor) {
        this.valor = valor;
    }

    public int getIndice() {
        return indice;
    }

    public void setIndice(int indice) {
        this.indice = indice;
    }
}